                    <p>Les <span>Glucides</span> (ou sucres) sont divisés en 2 catégories : Les glucides simples et les glucides complexes.
                        Les glucides simples font augmenter rapidement et fortement la glycémie : pour faire simple, on les retrouve dans tous les aliments qui ont un goût sucré comme les confiseries, pâtisseries, et les fruits.
                        Les glucides complexes sont également des sucres mais ils sont absorbés plus lentement par l’organisme et influencent aussi la glycémie mais de façon moins élevée et plus prolongée dans le temps. On les retrouve dans les féculents comme le riz, les pommes de terre, et tous les aliments à base de blé comme le pain et les pâtes.</p>
